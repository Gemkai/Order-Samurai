// Per-view config: color + opacity
// Overview is full brightness; pillar views are dimmer so content stays readable
const VIEW_CONFIG: Record<string, { color: string; opacity: number }> = {
  overview: { color: "var(--overview)",   opacity: 0.055 },
  bow:      { color: "var(--bow)",        opacity: 0.055 },
  sword:    { color: "var(--sword)",      opacity: 0.055 },
  brush:    { color: "var(--brush)",      opacity: 0.055 },
  arts:     { color: "var(--arts)",       opacity: 0.055 },
  reports:  { color: "var(--foreground)", opacity: 0.07 },
}

const VIEW_ART: Record<string, string> = {
  overview: OVERVIEW_ART,
  bow:      BOW_ART,
  sword:    SWORD_ART,
  brush:    BRUSH_ART,
  arts:     ARTS_ART,
  reports:  OVERVIEW_ART,
}

export function AsciiBackground({ view }: { view: string }) {
  const cfg = VIEW_CONFIG[view] ?? VIEW_CONFIG.overview
  const art = VIEW_ART[view] ?? VIEW_ART.overview
  const preRef = useRef<HTMLPreElement>(null)
  const [scale, setScale] = useState(1)
  const [flash, setFlash] = useState(false)
  const prevViewRef = useRef("")

  useEffect(() => {
    function measure() {
      const pre = preRef.current
      if (!pre) return
      const contentW = window.innerWidth - 230  // subtract sidebar
      const vh = window.innerHeight
      const aw = pre.scrollWidth
      const ah = pre.scrollHeight
      if (!aw || !ah) return
      // Fill 95% of height or 85% of content width, whichever constrains first
      const hScale = (vh * 0.95) / ah
      const wScale = (contentW * 0.85) / aw
      // Cap effective font at 24px so characters stay readable as art
      const maxScale = 24 / BASE_PX
      setScale(Math.min(hScale, wScale, maxScale) * 1.5)
    }
    const raf = requestAnimationFrame(measure)
    window.addEventListener("resize", measure)
    return () => { cancelAnimationFrame(raf); window.removeEventListener("resize", measure) }
  }, [art])

  // On view switch: spike opacity high (no transition), then fade back down over 1.4s
  useEffect(() => {
    if (!prevViewRef.current || prevViewRef.current === view) {
      prevViewRef.current = view
      return
    }
    prevViewRef.current = view
    setFlash(true)
    const t = setTimeout(() => setFlash(false), 60)
    return () => clearTimeout(t)
  }, [view])

  const peakOpacity = Math.min(cfg.opacity * 6, 0.38)

  return (
    <div
      aria-hidden="true"
      style={{
        position: "fixed",
        inset: 0,
        pointerEvents: "none",
        overflow: "hidden",
        zIndex: 0,
        display: "flex",
        alignItems: "flex-end",
        justifyContent: "center",
        opacity: flash ? peakOpacity : cfg.opacity,
        color: cfg.color,
        transition: flash
          ? "color 0.5s ease"
          : "opacity 1.4s ease-out, color 0.5s ease",
      } as CSSProperties}
    >
      <pre
        ref={preRef}
        key={view}
        style={{
          fontFamily: '"JetBrains Mono Variable", "JetBrains Mono", monospace',
          whiteSpace: "pre",
          userSelect: "none",
          margin: 0,
          padding: 0,
          lineHeight: 1,
          fontSize: `${BASE_PX}px`,
          color: "inherit",
          transformOrigin: "bottom center",
          transform: `scale(${scale})`,
          animation: "ascii-art-enter 0.5s ease",
        }}
      >
        {art}
      </pre>
    </div>
  )
}
